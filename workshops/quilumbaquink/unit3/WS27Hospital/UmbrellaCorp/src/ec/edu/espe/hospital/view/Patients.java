    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        BasicDBObject document=new BasicDBObject();
      
        String name;
        String lastName;
        int age;
        int id;
        String gender;
        String message;
        
        Patient patient;
        
        name=txtName.getText();
        lastName=txtLastName.getText();
        age=Integer.valueOf(txtAge.getText());
        id= Integer.valueOf(txtId.getText());
        gender=txtGender.getText();
        
        patient=new Patient(name, lastName, age , id, gender);
        message="Do you want to save \n" + name + "\n " + lastName + "\n" + age + "\n" + id + "\n" + gender+ "\n" ;
        // 0 YES, 1= NO , 2= CANCEL
        int option=JOptionPane.showConfirmDialog(this, message);
        if(option==0){
            System.out.println("Saving");
            document.put("Name", "'"+txtName.getText()+"'");
            document.put("LastName", "'"+txtLastName.getText()+"'");
            document.put("Age", Integer.valueOf(txtAge.getText()));
            document.put("id",Integer.valueOf(txtId.getText()) );
            document.put("Gender",txtGender.getText()+"'" );
            patients.insert(document);
            
            txtName.setText(null);
            txtLastName.setText(null);
            txtAge.setText(null);
            txtId.setText(null);
            txtGender.setText(null);
            
        }
        if(option==1){
             System.out.println("Not Save");
             txtName.setText(null);
             txtLastName.setText(null);
            txtAge.setText(null);
            txtId.setText(null);
            txtGender.setText(null);
        }
        if(option==2){
             System.out.println("Canceling");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

