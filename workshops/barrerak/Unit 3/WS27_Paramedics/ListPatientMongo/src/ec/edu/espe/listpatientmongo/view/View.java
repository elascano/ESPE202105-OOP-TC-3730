    public void shown() {
        String matrix[][] = new String[list.size()][4];
        for (int i = 0; i < list.size(); i++) {
            matrix[i][0] = list.get(i).getNameofPatient();
            matrix[i][1] = list.get(i).getAddress();
            matrix[i][2] = list.get(i).getDateOfAdmision();
            matrix[i][3] = list.get(i).getDeparturdDate();
        }
        jTableBook.setModel(new javax.swing.table.DefaultTableModel(
                matrix,
                new String[]{
                    "Name of Patient", "Address", "Date of Admision", "Departurd Date"
                }
        ));
    }

