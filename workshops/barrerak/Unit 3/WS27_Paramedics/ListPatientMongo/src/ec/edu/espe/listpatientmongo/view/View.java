    private static DBObject createDBObject(Patient patient) {
        BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
        docBuilder.append("_address", patient.getAddress());
        docBuilder.append("_nameofpatient", patient.getDateOfAdmision());
        docBuilder.append("_departurdDater", patient.getDeparturdDate());
        docBuilder.append("_nameofpatient", patient.getNameofPatient());

        return docBuilder.get();
    }

    private static Patient createPatiente(String nameofpatient, String address, String dateofadmision, String departurdDate) {
        Patient u = new Patient();
        u.setAddress(address);
        u.setNameofPatient(nameofpatient);
        u.setDeparturdDate(departurdDate);
        u.setDateOfAdmision(dateofadmision);
        return u;
    }

