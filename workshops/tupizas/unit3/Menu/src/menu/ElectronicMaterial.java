    public void mostra()
    {
        String matris [][] = new String [lista.size()][4];
        
        for (int i = 0; i < lista.size(); i++) {
            
            matris[i][0] = lista.get(i).getProducto();
            matris[i][1] = lista.get(i).getFabricador();
            matris[i][2] = lista.get(i).getId();
            matris[i][3] = lista.get(i).getQuantity();
 
        }
        
        jTableUser.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "Producto", "Fabricador", "Id", "Quantity"
            }
        ));
    }        
            
            
            
