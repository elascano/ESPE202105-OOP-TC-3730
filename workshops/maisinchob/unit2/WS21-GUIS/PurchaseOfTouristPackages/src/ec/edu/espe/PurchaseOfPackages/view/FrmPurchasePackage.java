    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        PurchaseOfPackages purchaseOfPackages;
        PurchaseOfPackagesController purchaseOfPackagesController;
        String cI = txtCI.getText();
        String names = txtNames.getText();
        String user = txtUser.getText();
        String password = pwdPassword.getText();
        String customerType = cmbCustomerType.getSelectedItem().toString();
        String purchaseDescription = txaDescription.getText();
        LifeInsurance[] lifeInsurance = new LifeInsurance[4];
        LocalDateTime PlanDate = LocalDateTime.now();
        
        purchaseOfPackages = new PurchaseOfPackages(cI, names, user, password, customerType, lifeInsurance, PlanDate, purchaseDescription);
       
        
        purchaseOfPackagesController = new PurchaseOfPackagesController(purchaseOfPackages, this);
        purchaseOfPackagesController.add(purchaseOfPackages);
        
        JOptionPane.showMessageDialog(rootPane, "adding + " + txtNames.getText());
    }//GEN-LAST:event_btnAddActionPerformed

