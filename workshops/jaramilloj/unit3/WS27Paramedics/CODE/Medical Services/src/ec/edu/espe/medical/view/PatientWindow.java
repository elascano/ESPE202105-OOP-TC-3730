    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        Patient patient;

        String name, lastName, email, gender, address;
        Integer age, phone;

        String message;

        name = txtName.getText();
        lastName = txtLastName.getText();
        age = Integer.valueOf(txtAge.getText());
        gender = cmbGender.getSelectedItem().toString();
        email = txtEmail.getText();
        address = txtAddress.getText();
        phone = Integer.valueOf(txtPhone.getText());

        patient = new Patient(name, lastName, age, gender, email, address, phone);

        patients.add(patient);

        message = "Do you want to save? \n " + name + "\n" + lastName
                + "\n" + age + "\n" + cmbGender.getSelectedItem() + "\n" + email
                + "\n" + address + "\n" + phone;

        //0 = yes, 1 = no, 2 = cancel
        int option = JOptionPane.showConfirmDialog(this, message);

        if (option == 0) {

            if (txtName.getText().length() == 0
                    || txtLastName.getText().length() == 0
                    || txtAge.getText().length() == 0 || cmbGender.getSelectedItem().toString() == null || txtEmail.getText().length() == 0
                    || txtAddress.getText().length() == 0 || txtPhone.getText().length() == 0) {

                JOptionPane.showConfirmDialog(null, "Enter Information", "OK", JOptionPane.DEFAULT_OPTION);
                btnAdd.enable(false);
            } else {
                btnAdd.enable(true);

                name = this.txtName.getText();
                lastName = this.txtLastName.getText();
                age = Integer.valueOf(txtAge.getText());
                gender = this.cmbGender.getSelectedItem().toString();
                email = this.txtEmail.getText();
                address = this.txtAddress.getText();
                phone = Integer.valueOf(txtPhone.getText());

                patientss.addPatientToBook(name, lastName, age, gender, email, address, phone);

            }

            System.out.println("Saving");

            this.txtName.setText("");
            this.txtLastName.setText("");
            this.txtAge.setText("");
            this.txtEmail.setText("");
            this.txtAddress.setText("");
            this.txtPhone.setText("");
        }
        if (option == 1) {
            System.out.println("Does not saved");
        }
        if (option == 2) {
            System.out.println("Cancelling");
        }


    }//GEN-LAST:event_btnAddActionPerformed

