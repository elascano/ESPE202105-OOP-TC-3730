    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

        String name;
        String email;
        int age;
        String adress;
        String specialization;

        Doctor doctor;
        name = this.txtName.getText();
        email = this.txtEmail.getText();
        age = Integer.valueOf(this.txtAge.getText());
        adress = this.txtAdress.getText();
        specialization = this.txtSpecialization.getText();

        doctors.add(name, email, age, adress, specialization);

        doctor = new Doctor(name, email, age, adress, specialization);

        String message = "Do you want to save \n" + name + "\n" + age + "\n" + adress + "\n" + email + "\n" + specialization + "\n";
        // 0 =yes, 1= No y 2= Cancel
        int option = JOptionPane.showConfirmDialog(rootPane, email);
        if (option == 0) {
            System.out.println("Saving");
        } else if (option == 1) {
            System.out.println("Not save");
        }
        if (option == 2) {
            System.out.println("Canceling");
        }

        txtName.setText("");
        txtAdress.setText("");
        txtAge.setText("");
        txtEmail.setText("");
        txtSpecialization.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed

