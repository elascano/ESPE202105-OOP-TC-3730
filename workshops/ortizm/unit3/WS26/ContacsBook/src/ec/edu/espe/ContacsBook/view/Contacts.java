    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        String name;
        Integer numberOfFriends;
        float salary;
        String email;
        Book book;
        String message;
        
    

        Contact contact;
        name = this.txtName.getText();
        numberOfFriends = Integer.valueOf(this.txtNumber.getText());
        salary = (float) Double.parseDouble(this.txtSalary.getText());
        email = this.txtEmail.getText();
        
       contacts.add(name, numberOfFriends, 0, email);
        
        contact = new Contact(name, numberOfFriends, 0, email);

        message = "Do you want to save \n" + name + "\n" + numberOfFriends + "\n" + salary + "\n" + email + cmbBook.getSelectedItem() + "\n";
        // 0 =yes, 1= No y 2= Cancel
        int option = JOptionPane.showConfirmDialog(rootPane, email);
        if (option == 0) {
            System.out.println("Saving");
        } else if (option == 1) {
            System.out.println("Not save");
        }
        if (option == 2) {
            System.out.println("Canceling");
        }

       
    txtName.setText(""); 
    txtNumber.setText("");
    txtSalary.setText("");
    txtEmail.setText("");
        

    }//GEN-LAST:event_btnOkActionPerformed

