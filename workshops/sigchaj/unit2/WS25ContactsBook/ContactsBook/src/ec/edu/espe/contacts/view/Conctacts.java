    private void btn_OkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_OkActionPerformed
        Contact contact;
        String name;
        Integer numerOfFriends;
        float salary;
        String email;
        Book book;
        String message;

        name = txt_Name.getText();
        numerOfFriends = Integer.valueOf(txt_NumberOfFriends.getText());
        salary = (float) Double.parseDouble(txt_Salary.getText());
        email = txt_Email.getText();
        contact = new Contact(name, numerOfFriends, salary, email);
        message = "Do you want to save? \n " + name + "\n" + numerOfFriends + "\n" + salary + "\n" + email + "\n" + cmb_Book.getSelectedItem() + "\n";
        // 0=YES , 1=NO , 2=Cancel
        int option = JOptionPane.showConfirmDialog(this, message);

        if (option == 0) {
            System.out.println("Saving");
        } else {
            if (option == 1) {
                System.out.println("Not save");
            }
                if (option == 2) {
                    System.out.println("Canceling");
                }
            }

    }//GEN-LAST:event_btn_OkActionPerformed

