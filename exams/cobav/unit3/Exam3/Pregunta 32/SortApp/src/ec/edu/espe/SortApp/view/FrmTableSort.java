    public void mostrar() {
        String matrix[][] = new String[arrList.size()][4];
        for (int i = 0; i < arrList.size(); i++) {
            matrix[i][0] = String.valueOf(arrList.get(i).getDatasNoOrdenated());
            matrix[i][1] = String.valueOf(arrList.get(i).getTam());
            matrix[i][2] = String.valueOf(arrList.get(i).getDatasOrdenated());
            matrix[i][3] = String.valueOf(arrList.get(i).getAlgorithm());
        }
        tblArray.setModel(new javax.swing.table.DefaultTableModel(
                matrix,
                new String[]{
                    "Numeros sin ordenar", "Tamaño del Arreglo", "Numeros ordenados", "Algoritmo"
                }
        ));

    }

