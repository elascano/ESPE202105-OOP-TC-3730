package ec.edu.espe.cellphone.model;

/**
 *
 * @author Andrea Quichimbo PROGRAM BUILDER ESPE-DCCO
 */
public class Additions {
    private int id;
    private String description;
    private boolean teamadditions;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the teamadditions
     */
    public boolean isTeamadditions() {
        return teamadditions;
    }

    /**
     * @param teamadditions the teamadditions to set
     */
    public void setTeamadditions(boolean teamadditions) {
        this.teamadditions = teamadditions;
    }
    
    
}
