    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
MongoDB mongoDB = MongoDB.getInstance();
        MongoDatabase  database = mongoDB.conecction();
        Document document = new Document();
        Validation validation = new Validation();

        document.put("user", txtUser.getText());
        document.put("password", txtPass.getText());

        if (validation.validate(txtUser.getText()) && validation.validate(txtPass.getText())) {
            int saveOption = JOptionPane.showConfirmDialog(rootPane, "Are you sure to print this information.?");
            MongoDB.save(document, "Customers",database);
            JOptionPane.showMessageDialog(rootPane, "Saved!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "User or Password are more than 8 characters");
        }


    }//GEN-LAST:event_btnSaveActionPerformed

