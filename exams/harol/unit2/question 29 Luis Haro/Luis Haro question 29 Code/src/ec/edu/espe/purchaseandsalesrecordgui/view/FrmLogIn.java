    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        ArrayList<User> users = new ArrayList<>();

        MongoDB mongoDB = MongoDB.getInstance();
        MongoDatabase database = mongoDB.conecction();
        Gson gson = new Gson();
        String json = "";
        try {
            json = MongoDB.completeModel("Customers", database);
        } catch (ParseException ex) { 
            Logger.getLogger(FrmLogIn.class.getName()).log(Level.SEVERE, null, ex);
        }

        java.lang.reflect.Type userType = new TypeToken<ArrayList<User>>() {
        }.getType();

        users = gson.fromJson(json, userType);
     
        
        for (int i = 0; i < users.size(); i++) {
            if (users.get(i).getUser().equals(txtUser.getText()) && users.get(i).getPass().equals(txtPass.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Log In sucessfull");
                return;
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Incorrect User or Password ");
    }//GEN-LAST:event_btnLogInActionPerformed

