    private void btnSortSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortSaveActionPerformed
        String num = txtNumbersArray.getText();
        String[] array;
        array = num.split(",");
        int[] numbers = new int[array.length];
        for (int i = 0; i < array.length; i++) {
            numbers[i] = Integer.parseInt(array[i]);
        }
        SortingContext sc = new SortingContext();
        int sortedList[] = sc.sort(numbers);
        txtSorted.setText(Arrays.toString(sortedList));
        txtSorting.setText(sc.getSortStrategy(numbers.length, numbers));
        Document document = new Document();
        document.put("unsorted", Arrays.toString(array));
        document.put("size", array.length);
        document.put("sort algorithm", sc.getSortStrategy(numbers.length, numbers));
        document.put("sorted", Arrays.toString(sortedList));
        MongoDatabase database = mongoDB.conecction("Luis","luis" ,"Conjunta");
        mongoDB.save(document, "Arrays", database);
        JOptionPane.showMessageDialog(null, "Data saved");
        
    }//GEN-LAST:event_btnSortSaveActionPerformed

