    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String user = txtUser.getText();
        String password = pswPassword.getText();

        try {
            Mongo mongo = new Mongo("localhost", 27017);
            dbMongo = mongo.getDB("Users");
            tableMongo = dbMongo.getCollection("InformationUsers");
        } catch (UnknownHostException ex) {
            Logger.getLogger(List.class.getName()).log(Level.SEVERE, null, ex);

        }

        BasicDBObject document = new BasicDBObject();
        document.put("User", user);
        document.put("Password", password);
        tableMongo.insert(document);

        JOptionPane.showMessageDialog(rootPane, "The information was saved correctly");
    }//GEN-LAST:event_jButton1ActionPerformed

