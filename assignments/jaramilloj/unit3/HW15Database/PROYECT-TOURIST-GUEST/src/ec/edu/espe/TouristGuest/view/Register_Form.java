    public void mostrar(){
        
        String matris[][]=new String[List.size()][7];
        for (int i = 0; i < List.size(); i++) {
            matris[i][0]=List.get(i).getFirstName();
            matris[i][1]=List.get(i).getLastName();
            matris[i][2]=List.get(i).getUserName();
            matris[i][3]=List.get(i).getAge();
            matris[i][4]=List.get(i).getAddress();
            matris[i][5]=List.get(i).getCi();
            matris[i][6]=List.get(i).getCellPhone();
            
        }
        TableUsers.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "First Name", "Last Name", "Username", "Age", "Address", "CI", "CellPhone"
            }
        ));
       
        
        
    }
    
