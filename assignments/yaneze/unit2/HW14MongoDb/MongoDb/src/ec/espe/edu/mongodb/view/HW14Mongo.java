    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        
        
        BasicDBObject document = new BasicDBObject();
        document.put("Product", "'" +txtProduct.getText()+ "'");
        document.put("Brand", "'" +txtBrand.getText()+ "'");
        document.put("Price", txtPrice.getText());
        document.put("Quantity", txtQuantity.getText());
        electronicmaterials.insert(document);

      
    }//GEN-LAST:event_saveButtonActionPerformed

    private void showButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonActionPerformed
        // TODO add your handling code here:
        DBCursor cursor = electronicmaterials.find();
        
        while(cursor.hasNext()){
            jTextArea1.setText(jTextArea1.getText() + "\n" + cursor.next());
            
        }

        
    }//GEN-LAST:event_showButtonActionPerformed

