    private void BtnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegisterActionPerformed
      
        ElectronicMaterial car = new ElectronicMaterial(txtId.getText(), txtMaterial.getText(), txtFabricator.getText(),txtQuantity.getText());
        ematerials.add(car);
        
        mostrar();
        txtId.setText("");
        txtMaterial.setText("");
        txtFabricator.setText("");
        txtQuantity.setText("");
    }
        
        public void mostrar(){
            String matris [][]= new String [ematerials.size()][4];
            
            for (int i = 0; i < ematerials.size(); i++) {
                
                matris[i][0]= ematerials.get(i).getID();
                matris[i][1]= ematerials.get(i).getMaterial();
                matris[i][2]= ematerials.get(i).getFabricator();
                matris[i][3]= ematerials.get(i).getQuantity();
                
            }
               jTableCars.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "ID", "Electronic Material", "Fabricator", "Quantity"
            }
        ));
        
     
     String Brand , Model, RegistrationNumber , Color;
    
    if (txtId.getText().length() ==0 || txtMaterial.getText().length() == 0 ||
            txtFabricator.getText().length() == 0 || txtQuantity.getText().length() == 0  ){
        
       JOptionPane.showConfirmDialog(null, "Enter Information" , "OK", JOptionPane.DEFAULT_OPTION);
       BtnRegister.enable(false);
    } else{
        BtnRegister.enable(true);
        Brand = this.txtId.getText();
        Model = this.txtMaterial.getText();
        RegistrationNumber = this.txtFabricator.getText();
        Color = this.txtQuantity.getText();
   
       ematerialss.add(Brand, Model,RegistrationNumber, Color);
      
    }
    txtId.setText("");
    txtMaterial.setText(""); 
    txtFabricator.setText("");
    txtQuantity.setText("");

        

    }//GEN-LAST:event_BtnRegisterActionPerformed

