    private static DBObject createDBObject(Book book) {
        BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
        docBuilder.append("_id", book.getID());
        docBuilder.append("name", book.getNameofBook());
        docBuilder.append("author", book.getAuthor());
        docBuilder.append("publicationDate", book.getPublicationName());

        return docBuilder.get();
    }

    private static Book createBook(String id, String name, String author, String date) {
        Book u = new Book();
        u.setID(id);
        u.setNameofBook(name);
        u.setAuthor(author);
        u.setPublicationName(date);
        return u;
    }

