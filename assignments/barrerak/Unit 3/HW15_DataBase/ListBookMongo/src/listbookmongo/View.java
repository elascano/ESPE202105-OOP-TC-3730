    public void shown() {
        String matrix[][] = new String[list.size()][4];
        for (int i = 0; i < list.size(); i++) {
            matrix[i][0] = list.get(i).getNameofBook();
            matrix[i][1] = list.get(i).getPublicationName();
            matrix[i][2] = list.get(i).getAuthor();
            matrix[i][3] = list.get(i).getID();
        }
        jTableBook.setModel(new javax.swing.table.DefaultTableModel(
                matrix,
                new String[]{
                    "Name of Book", "Publication Nmae", "Author", "ID"
                }
        ));
    }

