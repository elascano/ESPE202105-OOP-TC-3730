    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        BasicDBObject document=new BasicDBObject();
        document.put("Name","'"+ txtName.getText()+"'");
        document.put("Lastname","'"+ txtLastName.getText()+"'");
        document.put("Brithday","'"+ txtBirthday.getText()+"'");
        document.put("Phone",txtPhone.getText());
        tabla.insert(document);
             
        String []info=new String[4];
        info[0]=txtName.getText();
        info[1]=txtLastName.getText();
        info[2]=txtBirthday.getText();
        info[3]=txtPhone.getText();
        modelo.addRow(info);
        
        txtName.setText("");
        txtLastName.setText("");
        txtBirthday.setText("");
        txtPhone.setText(""); 
    }//GEN-LAST:event_btnRegisterActionPerformed

