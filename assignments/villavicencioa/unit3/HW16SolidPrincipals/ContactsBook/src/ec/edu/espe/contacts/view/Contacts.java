    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        Contact contact;
        String name;
        Integer numberOfFriends;
        float salary;
        String email;
        Book book;
        
        String message;

        name = txtName.getText();
        numberOfFriends = Integer.valueOf(txtNumberOfFriends.getText());
        salary = (float) Double.parseDouble(txtSalary.getText());
        email = txtEmail.getText();

        contact = new Contact(name, numberOfFriends, salary, email);
        
        message = "Do you want to save \n"+ name + "\n" + numberOfFriends + "\n" + salary + "\n" + email + "\n" + cmbBook.getSelectedItem() + "\n" ;
        //0 = Yes, 1 = No, 2= Cancel
        int option = JOptionPane.showConfirmDialog(this,message);
        
        if (option == 0){
            BasicDBObject document = new BasicDBObject();
            document.put("Name", "'" + txtName.getText() + "'");
            document.put("Number of Friends", "'" + Integer.valueOf(txtNumberOfFriends.getText()) + "'");
            document.put("Salary", "'" + "$" + txtSalary.getText() + "'");
            document.put("Email", "'" + txtEmail.getText() + "'");
            document.put("Book",cmbBook.getSelectedItem() + "'");
            txtName.setText(" ");
            txtNumberOfFriends.setText(" ");
            txtSalary.setText(" ");
            txtEmail.setText(" ");
            tabla.insert(document);
            System.out.println("Saving");
        }else{
            if (option == 1){
                txtName.setText(" ");
                txtNumberOfFriends.setText(" ");
                txtSalary.setText(" ");
                txtEmail.setText(" ");
                cmbBook.setSelectedItem(" ");
                System.out.println("Not Save");
            }
       
                if (option == 2){
                    txtName.setText(" ");
                    txtNumberOfFriends.setText(" ");
                    txtSalary.setText(" ");
                    txtEmail.setText(" ");
                    cmbBook.setSelectedItem(" ");
                    System.out.println("Calceling");   
                }
             }
    }//GEN-LAST:event_btnOkActionPerformed

