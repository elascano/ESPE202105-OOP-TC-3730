    public void mostrar()
    {
        String[][] matris = new String[list.size()][6];
        
          for (int i = 0; i < list.size();i++){
              
              matris[i][0]=list.get(i).getName();
              matris[i][1]=list.get(i).getLastName();
              matris[i][2]=list.get(i).getUserName();
              matris[i][3]=list.get(i).getCI();
              matris[i][4]=list.get(i).getCode();
              
          }
                jTableStudent.setModel(new javax.swing.table.DefaultTableModel(
           matris,
            new String [] {
                "Name", "LastName", "UserName", "CI", "Code"
            }
        ));  
    }
