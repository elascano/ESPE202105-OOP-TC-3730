    public void manifest(){
        String matrix [][]= new String[list.size()][4];
        
        for (int i = 0; i < list.size(); i++) {
            
            matrix [i][0]=list.get(i).getName();
            matrix [i][1]=list.get(i).getLastName();
            matrix [i][2]=list.get(i).getID();
            matrix [i][3]=list.get(i).getNumber();
            
        }
        jTableVehicle.setModel(new javax.swing.table.DefaultTableModel(
           matrix,
            new String [] {
                "Name", "Last Name", "I.D", "Number"
            }
        ));
    }
