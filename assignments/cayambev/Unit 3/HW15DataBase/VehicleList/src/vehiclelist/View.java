     private static DBObject createDBObject(Vehicle vehicle) {
        BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
        docBuilder.append("Name", vehicle.getName());
        docBuilder.append("LastName", vehicle.getLastName());
        docBuilder.append("ID", vehicle.getID());
        docBuilder.append("Number", vehicle.getNumber());

        return docBuilder.get();
    }
     private static Vehicle createVehicle(String name, String lastname, String id, String number) {
        Vehicle u = new Vehicle(name, lastname, id, number);
        u.setName(name);
        u.setLastName(lastname);
        u.setID(id);
        u.setNumber(number);
        return u;
    }

