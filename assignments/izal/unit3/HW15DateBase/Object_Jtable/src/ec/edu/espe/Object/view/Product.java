    private void buttonADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonADDActionPerformed
        String tickets,prices, qualitys, subtotals;
        int quality;
        double price,total,subtotal;
        quality = Integer.parseInt(Spnquality.getValue().toString());
        price = Double.parseDouble(txtprice.getText());
        subtotal = price * quality;
        total = Double.parseDouble(Text_total.getText());
        total = total + subtotal;
       
        tickets =txtCbo_ticket.getSelectedItem().toString();
        prices = "" + price;
        qualitys = "" + quality;
        subtotals = "" + subtotal;
        
        String data[]= {tickets,prices, qualitys, subtotals};
        model.addRow(data);
        
        Text_total.setText(""+ total);
        
        
        BasicDBObject document = new BasicDBObject();
        document.put("TICKET", " ´ "+txtCbo_ticket.getSelectedItem().toString()+"´");
        document.put("PRICE", " ´ "+txtprice.getText()+"´");
        document.put("QUALITY", " ´ "+Spnquality.getValue().toString()+"´");
        
        ticket.insert(document);
    }//GEN-LAST:event_buttonADDActionPerformed

