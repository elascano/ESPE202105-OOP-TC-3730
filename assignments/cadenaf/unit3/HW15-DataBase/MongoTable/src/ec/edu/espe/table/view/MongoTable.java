    private void BttUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BttUpdateActionPerformed
        DefaultTableModel tb = new DefaultTableModel();
        
        try {
            Mongo mongo = new Mongo("localhost", 27017);
            dbMongo = mongo.getDB("Cellphone");
            tableMongo = dbMongo.getCollection("Information the Cellphone");
        } catch (UnknownHostException ex) {
            Logger.getLogger(List.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        DBCursor cursor = tableMongo.find();
        DBCursor cursor1 = tableMongo.find();
        DBCursor cursor2 = tableMongo.find();
        DBCursor cursor3 = tableMongo.find();
        DBCursor cursor4 = tableMongo.find();
        DBCursor cursor5 = tableMongo.find();

        tb.addColumn("Client Name");
        tb.addColumn("ID");
        tb.addColumn("Phone");
        tb.addColumn("E-mail");
        tb.addColumn("Number of products purchased");
        tb.addColumn("Payment method");


        while (cursor.hasNext()) {
            tb.addRow(new Object[]{cursor.next().get("Client Name"),cursor1.next().get("ID"),
                cursor2.next().get("Phone:"),cursor3.next().get("E-mail:"),cursor4.next().get("Number of products purchased"), cursor5.next().get("Payment method")});
            TblTableCellphone.setModel(tb);
        }
    }//GEN-LAST:event_BttUpdateActionPerformed

