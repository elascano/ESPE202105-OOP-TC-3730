    private void BtmAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtmAddActionPerformed

        Integer id;
        double purchasePrice;
        double salePrice;
        Integer quantity;
        Integer idProvider;
        id = Integer.parseInt(txtId.getText());
        String name = txtName.getText();
        String brand = txtBrand.getText();
        purchasePrice = Double.parseDouble(txtId.getText());
        salePrice = Double.parseDouble(txtId.getText());
        quantity = Integer.valueOf(txtQuantity.getText());
        idProvider = Integer.valueOf(txtIdProvider.getText());

       Product product = new Product(id, name, brand, purchasePrice, salePrice, quantity, idProvider);
        products.add(product);

        mostrar();
        txtId.setText("");
        txtName.setText("");
        txtBrand.setText("");
        txtPurchasePrice.setText("");
        txtSalePrice.setText("");
        txtQuantity.setText("");
        txtIdProvider.setText("");
    }

    public void mostrar() {
        String matris[][] = new String[products.size()][7];

        for (int i = 0; i < products.size(); i++) {

            matris[i][0] = products.get(i).getId().toString();
            matris[i][1] = products.get(i).getName();
            matris[i][2] = products.get(i).getBrand();
            matris[i][3] = String.valueOf(products.get(i).getPurchasePrice());
            matris[i][4] = Double.toString(products.get(i).getSalePrice());
            matris[i][5] = products.get(i).getQuantity().toString();
            matris[i][6] = products.get(i).getIdProvider().toString();

        }
        jTableProduct.setModel(new javax.swing.table.DefaultTableModel(
                matris,
                new String[]{
                    "Brand", "Model", "Registration Number", "Color"
                }
        ));

        Integer id, quantity, idProvider;
        String name, brand;
        double purchasePrice, salePrice;

        if (txtId.getText().length() == 0 || txtName.getText().length() == 0
                || txtBrand.getText().length() == 0 || txtPurchasePrice.getText().length() == 0
                || txtSalePrice.getText().length() == 0 || txtQuantity.getText().length() == 0
                || txtIdProvider.getText().length() == 0) {

            JOptionPane.showConfirmDialog(null, "Enter Information", "OK", JOptionPane.DEFAULT_OPTION);
            BtmAdd.enable(false);

        } else {
            BtmAdd.enable(true);
            id = Integer.parseInt(this.txtId.getText());
            name = this.txtName.getText();
            brand = this.txtBrand.getText();
            purchasePrice = Double.valueOf(this.txtPurchasePrice.getText());
            salePrice = Double.valueOf(this.txtSalePrice.getText());
            quantity = Integer.parseInt(this.txtQuantity.getText());
            idProvider = Integer.parseInt(this.txtIdProvider.getText());
            productss.add(id, name, brand, purchasePrice, salePrice, quantity, idProvider);

        }
        txtId.setText("");
        txtName.setText("");
        txtBrand.setText("");
        txtPurchasePrice.setText("");
        txtSalePrice.setText("");
        txtQuantity.setText("");
        txtIdProvider.setText("");


    }//GEN-LAST:event_BtmAddActionPerformed

