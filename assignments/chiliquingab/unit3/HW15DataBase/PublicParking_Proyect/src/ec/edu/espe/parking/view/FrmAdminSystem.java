    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel tb = new DefaultTableModel();
        
        try {
            Mongo mongo = new Mongo("localhost", 27017);
            dbMongo = mongo.getDB("Users");
            tableMongo = dbMongo.getCollection("InformationUsers");
        } catch (UnknownHostException ex) {
            Logger.getLogger(List.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        DBCursor cursor0 = tableMongo.find();
        DBCursor cursor1 = tableMongo.find();
        DBCursor cursor2 = tableMongo.find();
        DBCursor cursor3 = tableMongo.find();

        tb.addColumn("FirstName");
        tb.addColumn("LastName");
        tb.addColumn("Age");
        tb.addColumn("Code");

        while (cursor0.hasNext()) {
            tb.addRow(new Object[]{cursor0.next().get("FirstName"),cursor1.next().get("LastName"),
                cursor2.next().get("Age"),cursor3.next().get("Code")});
            tblUser.setModel(tb);
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

