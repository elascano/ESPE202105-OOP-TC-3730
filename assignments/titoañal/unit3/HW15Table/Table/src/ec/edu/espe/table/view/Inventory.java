    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        ElectronicMaterial electronicMaterial = new ElectronicMaterial(txtProduct.getText(), txtPrice.getText(), txtFabricator.getText(),txtQuantity.getText());
        electronicMaterials.add(electronicMaterial);
        
        mostrar();
        txtProduct.setText("");
        txtPrice.setText("");
        txtFabricator.setText("");
        txtQuantity.setText("");
    }
        
        public void mostrar(){
            String matriz [][]= new String [electronicMaterials.size()][4];
            
            for (int i = 0; i < electronicMaterials.size(); i++) {
                
                matriz[i][0]= electronicMaterials.get(i).getProduct();
                matriz[i][1]= electronicMaterials.get(i).getPrice();
                matriz[i][2]= electronicMaterials.get(i).getFabricator();
                matriz[i][3]= electronicMaterials.get(i).getQuantity();
                
            }
               tabla.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Price", "Price", "Fabricator", "Quantity"
            }
        ));
        
     
     String Product , Price, Fabricator , Quantity;
    
    if (txtProduct.getText().length() ==0 || txtPrice.getText().length() == 0 ||
            txtFabricator.getText().length() == 0 || txtQuantity.getText().length() == 0  ){
        
       JOptionPane.showConfirmDialog(null, "Enter Information" , "OK", JOptionPane.DEFAULT_OPTION);
       btnInsert.enable(false);
    } else{
        btnInsert.enable(true);
        Product = this.txtProduct.getText();
        Price = this.txtPrice.getText();
        Fabricator = this.txtFabricator.getText();
        Quantity = this.txtQuantity.getText();
   
       inventoryControllers.add(Product, Price,Fabricator, Quantity);
      
    }
    txtProduct.setText("");
    txtPrice.setText(""); 
    txtFabricator.setText("");
    txtQuantity.setText("");
    }//GEN-LAST:event_btnInsertActionPerformed

