    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Electronic material = new Electronic(txtProduct.getText(), txtBrand.getText(), txtID.getText(),txtQuantity.getText());
        ematerials.add(material);
        
        mostrar();
        txtProduct.setText("");
        txtBrand.setText("");
        txtID.setText("");
        txtQuantity.setText("");
    }
        
        public void mostrar(){
            String matris [][]= new String [ematerials.size()][4];
            
            for (int i = 0; i < ematerials.size(); i++) {
                
                matris[i][0]= ematerials.get(i).getProduct();
                matris[i][1]= ematerials.get(i).getBrand();
                matris[i][2]= ematerials.get(i).getID();
                      
                matris[i][3]= ematerials.get(i).getQuantity();
                
            }
               jTableCars.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "Product", "Brand","ID","Quantity"
                    
                   
            }
        ));
        
     
     String Product ,Brand,ID,Quantity;
    
    if (txtProduct.getText().length() ==0 || txtID.getText().length() == 0 ||
            txtBrand.getText().length() == 0 || txtQuantity.getText().length() == 0  ){
        
       JOptionPane.showConfirmDialog(null, "Enter Information" , "OK", JOptionPane.DEFAULT_OPTION);
       btnAdd.enable(false);
    } else{
        btnAdd.enable(true);
        Product = this.txtProduct.getText();
        ID = this.txtID.getText();
        Brand = this.txtBrand.getText();
        Quantity = this.txtQuantity.getText();
   
       ematerialss.add(Product, ID,Brand, Quantity);
      
    }
    txtProduct.setText("");
    txtID.setText(""); 
    txtBrand.setText("");
    txtQuantity.setText("");

    }//GEN-LAST:event_btnAddActionPerformed

