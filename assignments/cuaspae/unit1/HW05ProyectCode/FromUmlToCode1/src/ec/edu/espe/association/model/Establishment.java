public class Establishment {
    private String name;
    private String location;
    private String numbreoplace;
    private String socurity;
    
    public void park (){
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the location
     */
    public String getLocation() {
        return location;
    }

    /**
     * @param location the location to set
     */
    public void setLocation(String location) {
        this.location = location;
    }

    /**
     * @return the numbreoplace
     */
    public String getNumbreoplace() {
        return numbreoplace;
    }

    /**
     * @param numbreoplace the numbreoplace to set
     */
    public void setNumbreoplace(String numbreoplace) {
        this.numbreoplace = numbreoplace;
    }

    /**
     * @return the socurity
     */
    public String getSocurity() {
        return socurity;
    }

    /**
     * @param socurity the socurity to set
     */
    public void setSocurity(String socurity) {
        this.socurity = socurity;
    }
    
}
