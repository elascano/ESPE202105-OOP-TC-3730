    private void BtnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegisterActionPerformed
      
        Car car = new Car(txtBrand.getText(), txtModel.getText(), txtNumber.getText(),txtColor.getText());
        cars.add(car);
        
        mostrar();
        txtBrand.setText("");
        txtModel.setText("");
        txtNumber.setText("");
        txtColor.setText("");
    }
        
        public void mostrar(){
            String matris [][]= new String [cars.size()][4];
            
            for (int i = 0; i < cars.size(); i++) {
                
                matris[i][0]= cars.get(i).getBrand();
                matris[i][1]= cars.get(i).getModel();
                matris[i][2]= cars.get(i).getRegistrationNumber();
                matris[i][3]= cars.get(i).getColor();
                
            }
               jTableCars.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "Brand", "Model", "Registration Number", "Color"
            }
        ));
        
     
     String Brand , Model, RegistrationNumber , Color;
    
    if (txtBrand.getText().length() ==0 || txtModel.getText().length() == 0 ||
            txtNumber.getText().length() == 0 || txtColor.getText().length() == 0  ){
        
       JOptionPane.showConfirmDialog(null, "Enter Information" , "OK", JOptionPane.DEFAULT_OPTION);
       BtnRegister.enable(false);
    } else{
        BtnRegister.enable(true);
        Brand = this.txtBrand.getText();
        Model = this.txtModel.getText();
        RegistrationNumber = this.txtNumber.getText();
        Color = this.txtColor.getText();
   
       carss.add(Brand, Model,RegistrationNumber, Color);
      
    }
    txtBrand.setText("");
    txtModel.setText(""); 
    txtNumber.setText("");
    txtColor.setText("");

        

    }//GEN-LAST:event_BtnRegisterActionPerformed

