    private void btnRegistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrerActionPerformed
        // TODO add your handling code here:
        Vehicle vehicle = new Vehicle(txtBrand.getText(), txtModel.getText(), txtPrice.getText());
        list.add(vehicle);
        shown();
        try {
            // TODO add your handling code here:
            MongoClient mongo = new MongoClient("localhost", 27017);
            DB db = mongo.getDB("vhcle");
            DBCollection col = db.getCollection("car");
            DBObject doc = createDBObject(vehicle);
            WriteResult result = col.insert(doc);
            JOptionPane.showMessageDialog(null, "Registered book successfully");

        } catch (UnknownHostException ex) {

        }

    }//GEN-LAST:event_btnRegistrerActionPerformed

    public void shown() {
        String matrix[][] = new String[list.size()][4];
        for (int i = 0; i < list.size(); i++) {
            matrix[i][0] = list.get(i).getBrand();
            matrix[i][1] = list.get(i).getModel();
            matrix[i][2] = list.get(i).getPrice();
        }
        jTableBook.setModel(new javax.swing.table.DefaultTableModel(
                matrix,
                new String[]{
                    "Brand of vehicle", "Model of vehicle", "Price"
                }
        ));
    }

