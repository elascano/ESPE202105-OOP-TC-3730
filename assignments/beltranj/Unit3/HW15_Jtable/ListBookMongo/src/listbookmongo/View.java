    private static DBObject createDBObject(Vehicle vehicle) {
        BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
        docBuilder.append("Brand", vehicle.getBrand());
        docBuilder.append("Model", vehicle.getModel());
        docBuilder.append("Price", vehicle.getPrice());
     

        return docBuilder.get();
    }

    private static Vehicle createBook(String brand, String model, String price) {
        Vehicle v = new Vehicle(brand, model, price);
        v.setBrand(brand);
        v.setModel(model);
        v.setPrice(price);
        return v;
    }

